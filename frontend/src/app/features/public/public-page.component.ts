function isPublicPageKey(value: unknown): value is PublicPageKey {
  return typeof value === 'string' && value in PAGE_CONTENT;
}

@Component({
  selector: 'app-public-page',
  imports: [AdminLayoutComponent, FormsModule, RouterLink],
  templateUrl: './public-page.component.html',
  styleUrl: './public-page.component.scss'
})
export class PublicPageComponent {
  private readonly route = inject(ActivatedRoute);
  private readonly routeData = toSignal(this.route.data, { initialValue: this.route.snapshot.data });

  readonly publicLinks = PUBLIC_LINKS;
  readonly paymentServiceName = PAYMENT_SERVICE_NAME;
  readonly paymentStatus = signal('');
  readonly paymentDraft = {
    amount: null as number | null,
    email: '',
    comment: '',
    offerConsent: false,
    receiptConsent: false
  };

  readonly pageKey = computed<PublicPageKey>(() => {
    const page = this.routeData()['page'];
    return isPublicPageKey(page) ? page : 'services';
  });

  readonly page = computed(() => PAGE_CONTENT[this.pageKey()]);

  isActiveLink(link: PublicLink): boolean {
    return link.href === PAGE_PATHS[this.pageKey()];
  }

  canPreparePayment(): boolean {
    return Boolean(
      this.paymentDraft.amount &&
      this.paymentDraft.amount > 0 &&
      this.paymentDraft.email.trim() &&
      this.paymentDraft.offerConsent &&
      this.paymentDraft.receiptConsent
    );
  }

  paymentAmountLabel(): string {
    const amount = this.paymentDraft.amount;
    if (!amount || amount <= 0) {
      return '0 ₽';
    }

    return `${new Intl.NumberFormat('ru-RU', {
      minimumFractionDigits: 0,
      maximumFractionDigits: 2
    }).format(amount)} ₽`;
  }

  preparePayment(): void {
    if (!this.canPreparePayment()) {
      this.paymentStatus.set('Заполните сумму, e-mail и подтвердите согласия.');
      return;
    }

    this.paymentStatus.set('Шаблон готов. На следующем этапе здесь появится переход к оплате через Т-Банк.');
  }
}
