@Component({
  selector: 'app-training',
  imports: [AdminLayoutComponent, RouterLink],
  templateUrl: './training.component.html',
  styleUrl: './training.component.scss',
})
export class TrainingComponent {
  private readonly auth = inject(AuthService);
  private readonly tabOrder: readonly TrainingAudience[] = ['manager', 'worker'];

  readonly requestedTab = signal<TrainingAudience>('manager');
  readonly visibleTabs = computed(() => this.tabOrder.filter((tab) => this.canSeeTab(tab)));
  readonly activeTabKey = computed<TrainingAudience>(() => {
    const requested = this.requestedTab();
    return this.canSeeTab(requested) ? requested : (this.visibleTabs()[0] ?? 'manager');
  });
  readonly activeTab = computed(() => TRAINING_TABS[this.activeTabKey()]);

  selectTab(tab: TrainingAudience): void {
    if (this.canSeeTab(tab)) {
      this.requestedTab.set(tab);
    }
  }

  canSeeTab(tab: TrainingAudience): boolean {
    this.auth.tokenParsed();
    return this.auth.hasAnyRealmRole(TRAINING_TABS[tab].roles);
  }

  trackTab(_index: number, tab: TrainingAudience): TrainingAudience {
    return tab;
  }

  trackShortcut(_index: number, shortcut: TrainingShortcut): string {
    return shortcut.routerLink;
  }

  trackFlowStep(_index: number, step: TrainingFlowStep): string {
    return step.title;
  }

  trackBlock(_index: number, block: TrainingBlock): string {
    return block.title;
  }

  trackItem(_index: number, item: string): string {
    return item;
  }
}
